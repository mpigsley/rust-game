pub struct App {
    gl: GlGraphics, // OpenGL drawing backend.
}

impl App {
    fn render(&mut self, args: &RenderArgs) {
        use graphics::*;

        const WHITE: [f32; 4] = [1.0, 1.0, 1.0, 1.0];
        const GREY: [f32; 4] = [0.5, 0.5, 0.5, 1.0];

        self.gl.draw(args.viewport(), |c, gl| {
            clear(WHITE, gl);

            for (i, &row) in GAME_MAP.iter().enumerate() {
                for (j, &game_piece) in row.iter().enumerate() {
                    if game_piece == 1 {
                        let square = rectangle::square((j * 25) as f64, (i * 25) as f64, 25.0);
                        rectangle(GREY, square, c.transform, gl);
                    }
                }
            }
        });
    }

    // fn update(&mut self, args: &UpdateArgs) {

    // }
}

fn main() {
    // Change this to OpenGL::V2_1 if not working.
    let opengl = OpenGL::V3_2;

    // Create an Glutin window.
    let mut window: Window = WindowSettings::new("spinning-square", [700, 500])
        .opengl(opengl)
        .exit_on_esc(true)
        .build()
        .unwrap();

    // Create a new game and run it.
    let mut app = App {
        gl: GlGraphics::new(opengl),
    };

    let mut events = Events::new(EventSettings::new());
    while let Some(e) = events.next(&mut window) {
        if let Some(r) = e.render_args() {
            app.render(&r);
        }

        // if let Some(u) = e.update_args() {
        //     app.update(&u);
        // }
    }
}
