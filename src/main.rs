const TILE_SIZE: f64 = 25.0;


pub struct App {
    gl: GlGraphics, // OpenGL drawing backend.
    up: bool, down: bool, left: bool, right: bool, // direction indicators
    px: f64, py: f64, // player position
    pxd: f64, pyd: f64, // player deltas
}

impl App {
    fn render(&mut self, args: &RenderArgs) {
        use graphics::*;

        const WHITE: [f32; 4] = [1.0, 1.0, 1.0, 1.0];
        const GREY: [f32; 4] = [0.5, 0.5, 0.5, 1.0];
        const RED: [f32; 4] = [1.0, 0.0, 0.0, 1.0];

        let px = self.px;
        let py = self.py;

        println!("px: {:?}", px);
        println!("py: {:?}", py);

        self.gl.draw(args.viewport(), |c, gl| {
            clear(WHITE, gl);

            // print map
            for (i, &row) in GAME_MAP.iter().enumerate() {
                for (j, &game_piece) in row.iter().enumerate() {
                    if game_piece == 1 {
                        let square = rectangle::square((j * 25) as f64, (i * 25) as f64, TILE_SIZE);
                        rectangle(GREY, square, c.transform, gl);
                    }
                }
            }
            // print player
            let player = rectangle::square((px * TILE_SIZE) as f64, (py * TILE_SIZE) as f64, TILE_SIZE);
            rectangle(RED, player, c.transform, gl);
        });
    }

    fn update(&mut self, _args: &UpdateArgs) {
        if self.up && self.py >= 0.0 {
            self.py -= self.pyd;
        }
        if self.down && self.py <= 19.0 { 
            self.py += self.pyd; 
        }
        if self.left && self.px >= 0.0 { 
            self.px -= self.pxd; 
        }
        if self.right && self.px <= 27.0 { 
            self.px += self.pxd; 
        }
    }

    fn input(&mut self, _args: &ButtonArgs) {
        // println!("{:?}", _args.state);
        // println!("{:?}", _args.button);
        // println!("{:?}", _args.scancode);
        match _args.button {
            Button::Keyboard(Key::Up) => {
                self.up =_args.state == ButtonState::Press;
            },
            Button::Keyboard(Key::Down) => {
                self.down =_args.state == ButtonState::Press;
            },
            Button::Keyboard(Key::Left) => {
                self.left =_args.state == ButtonState::Press;
            },
            Button::Keyboard(Key::Right) => {
                self.right =_args.state == ButtonState::Press;
            },
            _ => {}

        }
    }
}

fn main() {
    // Change this to OpenGL::V2_1 if not working.
    let opengl = OpenGL::V3_2;

    // Create an Glutin window.
    let mut window: Window = WindowSettings::new("spinning-square", [700, 500])
        .opengl(opengl)
        .exit_on_esc(true)
        .build()
        .unwrap();

    // Create a new game and run it.
    let mut app = App {
        gl: GlGraphics::new(opengl),
        up: false, down: false, left: false, right: false,
        px: 0.0, py: 18.0,
        pxd: 0.5, pyd: 0.5,
    };

    let mut events = Events::new(EventSettings::new());
    while let Some(e) = events.next(&mut window) {
        if let Some(r) = e.render_args() {
            app.render(&r);
        }
        if let Some(u) = e.update_args() {
            app.update(&u);
        }
        if let Some(b) = e.button_args() {
            app.input(&b);
        }
    }
}
